import type { ActionFunctionArgs, LoaderFunction } from "@remix-run/node";
import { json, Link, redirect } from "@remix-run/react";
import { deleteAccount, getUserSnapshot } from "~/services/auth";
import { auth } from "~/services/firebase";

export const loader: LoaderFunction = async () => {
  try {
    const user = auth.currentUser;
    if (user) {
      const userSnapshot = await getUserSnapshot(user.uid); // Ambil data user dari Firebase Firestore
      if (userSnapshot.exists()) {
        console.log("bisa");
        return redirect("/admin"); // Redirect jika user ditemukan
      }
    }
    return null; // Jika user tidak ditemukan, lanjutkan tanpa redirect
  } catch (error) {
    console.log(error);
    return null; // Tangani error dan lanjutkan jika perlu
  }
};
export async function action({ request }: ActionFunctionArgs) {
  const formData = await request.formData();
  const email = formData.get("email") as string;
  const password = formData.get("password") as string;
  try {
    await deleteAccount(email, password);

    return redirect("/login");
  } catch (error) {
    console.log(error);
    return json({ error: (error as Error).message, status: 400 });
  }
}

